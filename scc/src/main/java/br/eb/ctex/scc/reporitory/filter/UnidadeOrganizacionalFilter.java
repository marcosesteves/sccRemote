package br.eb.ctex.scc.reporitory.filter;

public class UnidadeOrganizacionalFilter {
	
	private String siglaUnidade;
	private String nomeUidade;
	
	public String getSiglaUnidade() {
		return siglaUnidade;
	}
	public void setSiglaUnidade(String siglaUnidade) {
		this.siglaUnidade = siglaUnidade;
	}
	public String getNomeUidade() {
		return nomeUidade;
	}
	public void setNomeUidade(String nomeUidade) {
		this.nomeUidade = nomeUidade;
	}

}
